/**
 * This class is used for basic arithmetical  operations with real numbers.
 * @author asemenov
 * @since 28.09.2019
 * @version 1
 */
public class Calculator {

    /**
     * Addition operation.
     * @param first first argument.
     * @param second second argument.
     * @return result of operation.
     */
    public double addition(double first, double second) {
        return first + second;
    }

    /**
     * Subtraction operation.
     * @param first first argument.
     * @param second second argument.
     * @return result of operation.
     */
    public double subtraction(double first, double second) {
        return first - second;
    }

    /**
     * Multiplication operation.
     * @param first first argument.
     * @param second second argument.
     * @return result of operation.
     */
    public double multiplication(double first, double second) {
        return first * second;
    }

    /**
     * Division operation.
     * @param first first argument.
     * @param second second argument.
     * @return result of operation.
     */
    public double division(double first, double second) {
        return first / second;
    }
}
